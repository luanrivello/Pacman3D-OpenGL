package Projeto3D;

/**
 *
 * @author Mago Negro
 */
public class Ponto2V {
    private double x;
    private double z;

    public Ponto2V(double x, double z) {
        this.x = x;
        this.z = z;
    }
    
    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }
    
    
}
